                        <div class="strategic-li">
                            <h3>Step 4: Implemented SEO for Organic Growth</h3>
                            <p>
                                Post-migration, we focused on SEO. Detailed traffic and competitor research helped us optimize category pages and top-selling products. This hybrid UX strategy bridged online and physical retail experiences.
                            </p>
                        </div>
                    </li>
                    <li>
                        <span>5</span>
                        <div class="strategic-li">
                            <h3>Step 5: Enhanced Site Search and UX</h3>
